void positive_or_negative(int i)
/**
 * positive or negative
 */
{
	if (i > 0)
	{
	printf("%d is positive\n", i);
	}
	else if (i < 0)
	{
	printf("%d is negative\n", i);
	}
	else
	printf("%d is zero\n", i);

}
